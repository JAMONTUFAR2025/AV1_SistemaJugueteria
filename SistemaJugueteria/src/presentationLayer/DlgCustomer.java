    private void tblCustomersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCustomersMouseClicked
        // TODO add your handling code here:
        int filaSelec = tblCustomers.getSelectedRow();
        if (filaSelec >= 0) {
            // Llenamos solo los campos que existen en tu formulario
            txtId.setText(tblCustomers.getValueAt(filaSelec, 0).toString());
            txtFirstName.setText(tblCustomers.getValueAt(filaSelec, 1).toString());
            txtLastName.setText(tblCustomers.getValueAt(filaSelec, 2).toString());
            txtAge.setText(tblCustomers.getValueAt(filaSelec, 3).toString());
            cboGender.setSelectedItem(tblCustomers.getValueAt(filaSelec, 4).toString());
            txtDNI.setText(tblCustomers.getValueAt(filaSelec, 5).toString());
            txtPhoneNumber.setText(tblCustomers.getValueAt(filaSelec, 6).toString());
            txtAddress.setText(tblCustomers.getValueAt(filaSelec, 7).toString());

            // El stock (columna 8) se ignora porque no tienes donde mostrarlo
            isEditing = true; 
            
            // Si hacen doble clic, mandamos el objeto de vuelta
            if (evt.getClickCount() == 2 && listener != null) {
                E_Customer selected = new E_Customer();
                selected.setId(Integer.parseInt(tblCustomers.getValueAt(filaSelec, 0).toString()));
                selected.setFirstName(tblCustomers.getValueAt(filaSelec, 1).toString());
                selected.setLastName(tblCustomers.getValueAt(filaSelec, 2).toString());
                // No son necesarios mas campos
                
                listener.onObjectSelected(selected);
                this.dispose();
            }
        }
    }//GEN-LAST:event_tblCustomersMouseClicked

