    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        // 1. Validación de campos obligatorios
        if(txtFirstName.getText().trim().isEmpty() 
                || txtLastName.getText().trim().isEmpty() 
                || txtDNI.getText().trim().isEmpty() 
                || txtAge.getText().trim().isEmpty()
                || txtPhoneNumber.getText().trim().isEmpty()
                || txtAddress.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, llene todos los campos.");
            return;
        }
        
        try
        {
            // 2. Mapeo de datos al objeto customer
            // Nota: Asegúrate de que el objeto 'customer' esté instanciado previamente
            customer.setFirstName(txtFirstName.getText());
            customer.setLastName(txtLastName.getText());
            customer.setAge(Integer.parseInt(txtAge.getText())); // Conversión a entero
            customer.setGender(cboGender.getSelectedItem().toString());
            customer.setDni(txtDNI.getText());
            customer.setPhoneNumber(txtPhoneNumber.getText());
            customer.setAddress(txtAddress.getText());

            // 3. Lógica de persistencia (Insertar o Actualizar)
            if (isEditing) {
                // ACTUALIZAR: Usamos el ID del campo txtId
                customer.setId(Integer.parseInt(txtId.getText()));

                if (factory.update(customer)) {
                    JOptionPane.showMessageDialog(this, "Cliente actualizado con éxito.");
                    resetForm();
                }
            } else {
                // INSERTAR: Registro nuevo
                if (factory.insert(customer)) {
                    JOptionPane.showMessageDialog(this, "Cliente registrado con éxito.");
                    resetForm();
                }
            }
        }
        catch(NumberFormatException e)
        {
            JOptionPane.showMessageDialog(this, "Por favor, introduce valores validos en los campos numericos.");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (!txtId.getText().isEmpty() && isEditing) {
            int confirm = JOptionPane.showConfirmDialog(this, "¿Desea eliminar este producto?", "Confirmar", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                int id = Integer.parseInt(txtId.getText());
                if (factory.delete(id)) {
                    JOptionPane.showMessageDialog(this, "Producto eliminado.");
                    resetForm();
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un registro de la tabla para eliminar.");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

