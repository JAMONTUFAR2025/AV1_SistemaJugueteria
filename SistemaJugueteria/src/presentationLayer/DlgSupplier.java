    private void tblSuppliersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSuppliersMouseClicked
        // TODO add your handling code here:
        int filaSelec = tblSuppliers.getSelectedRow();
        if (filaSelec >= 0) {
            // Asignar a los campos de texto
            txtId.setText(tblSuppliers.getValueAt(filaSelec, 0).toString());
            txtCompanyName.setText(tblSuppliers.getValueAt(filaSelec, 1).toString());
            txtPhoneNumber.setText(tblSuppliers.getValueAt(filaSelec, 2).toString());
            txtEmail.setText(tblSuppliers.getValueAt(filaSelec, 3).toString());

            // Importante: No dejar que el proveedor piense que es un nuevo registro
            txtId.setEditable(false);
            isEditing = true; // <--- Cambiamos a modo edición
        }
    }//GEN-LAST:event_tblSuppliersMouseClicked

