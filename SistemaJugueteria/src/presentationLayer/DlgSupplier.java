    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (txtCompanyName.getText().trim().isEmpty() 
                || txtPhoneNumber.getText().trim().isEmpty()
                || txtEmail.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios. Por favor, complete la información.");
            return;
        }

        try {
            supplier.setId(Integer.parseInt(txtId.getText())); 
            supplier.setCompanyName(txtCompanyName.getText());
            supplier.setPhoneNumber(txtPhoneNumber.getText()); 
            supplier.setEmail(txtEmail.getText());

            if (isEditing) {
                if (factory.update(supplier)) {
                    JOptionPane.showMessageDialog(this, "Proveedor actualizado con éxito.");
                    resetForm();
                }
            } else {
                if (factory.insert(supplier)) {
                    JOptionPane.showMessageDialog(this, "Proveedor registrado con éxito.");
                    resetForm();
                }
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Ocurrio un error " + e.getMessage());
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (!txtId.getText().isEmpty() && isEditing) {
            int confirm = JOptionPane.showConfirmDialog(this, "¿Seguro que desea eliminar este proveedor?", "Confirmar", javax.swing.JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                int id = Integer.parseInt(txtId.getText());
                if (factory.delete(id)) {
                    JOptionPane.showMessageDialog(this, "Proveedor eliminado.");
                    cleanFields();
                    loadTable("");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un proveedor de la tabla para eliminar.");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

