    private void calculateTotals() {
        double subTotalGeneral = 0.0;
        double isvGeneral = 0.0;

        // Recorrer todas las filas de la tabla carrito
        for (int i = 0; i < tblCart.getRowCount(); i++) {
            // Obtenemos los valores de las columnas ISV (índice 4) y SubTotal (índice 5)
            double isvFila = Double.parseDouble(tblCart.getValueAt(i, 4).toString());
            double subTotalFila = Double.parseDouble(tblCart.getValueAt(i, 5).toString());

            isvGeneral += isvFila;
            subTotalGeneral += subTotalFila;
        }

        double totalPagar = subTotalGeneral + isvGeneral;

        // Actualizar los campos de texto en la parte inferior (suponiendo sus nombres)
        // Usamos String.format para dejarlo con 2 decimales
        txtSubtotal.setText(String.format("%.2f", subTotalGeneral));
        txtResultTax.setText(String.format("%.2f", isvGeneral));
        txtTotal.setText(String.format("%.2f", totalPagar));
    }
    
    private void resetSaleForm() {
        // 1. Limpiar campos de texto del encabezado
        txtCustomerCode.setText("");
        txtCustomer.setText(""); // Nombre del cliente
        txtBillNumber.setText("");

        // 2. Limpiar cajas de texto de los juguetes
        clearToyFields();
        selectedToyId = -1; // Resetear la variable global

        // 3. Vaciar la tabla (Carrito)
        cart.setRowCount(0); 

        // 4. Poner totales en 0
        calculateTotals(); 

        // Opcional: Actualizar la fecha a la de hoy por si se quedó abierto de un día a otro
        txtDate.setText(String.valueOf(java.time.LocalDate.now()));
    }
    
