    private void btnAddToyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddToyActionPerformed
        // TODO add your handling code here:
        // 1. Validaciones iniciales
        if (selectedToyId == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un juguete.");
            return;
        }

        int filaExistente = -1;
        // 2. BUSCAR si el ID ya está en la tabla (Columna 0)
        for (int i = 0; i < tblCart.getRowCount(); i++) {
            int idEnTabla = Integer.parseInt(tblCart.getValueAt(i, 0).toString());
            if (idEnTabla == selectedToyId) {
                filaExistente = i; // Guardamos el índice de la fila encontrada
                break;
            }
        }
        
        // 3. Obtener y validar valores de los campos de texto
        double precio = Double.parseDouble(txtPrice.getText());
        double porcentajeIsv = Double.parseDouble(txtTax.getText());
        int cantidadNueva;
        int stockActual;

        // Usamos un bloque try-catch por si el usuario escribe letras por accidente
        try {
            cantidadNueva = Integer.parseInt(txtQuantity.getText());
            stockActual = Integer.parseInt(txtStock.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor ingrese un número entero válido para la cantidad.", "Error de entrada", JOptionPane.ERROR_MESSAGE);
            return; // Detiene la ejecución para que no ocurra un desastre
        }

        // Validar que no sea 0 o negativo
        if (cantidadNueva <= 0) {
            JOptionPane.showMessageDialog(this, "La cantidad a vender debe ser al menos 1.", "Cantidad Inválida", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Validar que no estemos vendiendo más de lo que hay en bodega
        if (cantidadNueva > stockActual) {
            JOptionPane.showMessageDialog(this, "No hay suficiente inventario. Solo tienes " + stockActual + " en stock.", "Stock Insuficiente", JOptionPane.WARNING_MESSAGE);
            return;
        }

        double subTotalFila = precio * cantidadNueva;
        double isvFila = subTotalFila * (porcentajeIsv / 100.0);

        if (filaExistente != -1) {
            // --- CASO A: EL JUGUETE YA ESTÁ EN LA TABLA ---
            // Simplemente actualizamos los valores de esa fila específica
            cart.setValueAt(cantidadNueva, filaExistente, 3); // Columna Cantidad
            cart.setValueAt(isvFila, filaExistente, 4);      // Columna ISV
            cart.setValueAt(subTotalFila, filaExistente, 5); // Columna Sub-Total
        } else {
            // --- CASO B: ES UN JUGUETE NUEVO ---
            Object[] fila = new Object[6];
            fila[0] = selectedToyId;
            fila[1] = txtToy.getText();
            fila[2] = precio;
            fila[3] = cantidadNueva;
            fila[4] = isvFila;
            fila[5] = subTotalFila;
            cart.addRow(fila);
        }

        // 4. Limpiar campos y recalcular
        clearToyFields(); // Método auxiliar para vaciar txtToy, txtPrice, etc.
        calculateTotals();
    }//GEN-LAST:event_btnAddToyActionPerformed

