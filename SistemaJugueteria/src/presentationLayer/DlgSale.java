    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        // ==========================================
        // 1. VALIDACIONES
        // ==========================================
        // Asumiendo que txtCustomerCode es donde guardaste el ID del cliente y txtBillNumber el número de factura
        if (txtCustomerCode.getText().isEmpty() || txtBillNumber.getText().isEmpty() || txtDate.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, complete los datos del cliente y el número de factura.");
            return;
        }

        if (tblCart.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "El carrito está vacío. Agregue al menos un producto.");
            return;
        }

        // ==========================================
        // 2. GUARDAR EL MAESTRO (Sales)
        // ==========================================
        E_Sale sale = new E_Sale();
        sale.setNumberOfBill(txtBillNumber.getText());
        sale.setCustomerId(Integer.parseInt(txtCustomerCode.getText()));
        sale.setUserId(loggedUser.getId()); 

        // Convertir el texto de la fecha (ej. "2026-04-10") a java.sql.Date
        try {
            sale.setSaleDate(java.sql.Date.valueOf(txtDate.getText())); 
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this, "Formato de fecha inválido.");
            return;
        }

        F_Sale factorySales = new F_Sale();
        int idVentaGenerado = factorySales.insert(sale); // Recordamos que este método retorna el ID

        // ==========================================
        // 3. GUARDAR LOS DETALLES (Sales_Details)
        // ==========================================
        if (idVentaGenerado > 0) {
            F_SaleDetail factoryDetails = new F_SaleDetail();
            boolean erroresEnDetalles = false;

            // Recorremos la tabla fila por fila
            for (int i = 0; i < tblCart.getRowCount(); i++) {
                E_SaleDetail detalle = new E_SaleDetail();

                // Asignamos el ID de la factura que acabamos de crear
                detalle.setSaleId(idVentaGenerado); 

                // Extraemos los datos de la fila actual (Asegúrate de que los índices coincidan con tu modelo)
                // Índice 0: Toy ID | Índice 2: Price | Índice 3: Quantity
                detalle.setToyId(Integer.parseInt(tblCart.getValueAt(i, 0).toString()));
                detalle.setPrice(Double.parseDouble(tblCart.getValueAt(i, 2).toString()));
                detalle.setQuantity(Integer.parseInt(tblCart.getValueAt(i, 3).toString()));

                // Guardamos el detalle en la base de datos
                if (!factoryDetails.insert(detalle)) {
                    erroresEnDetalles = true;
                }
                
                int toyId = Integer.parseInt(tblCart.getValueAt(i, 0).toString());
                int quantitySold = Integer.parseInt(tblCart.getValueAt(i, 3).toString());
                        
                // Actualizar cantidad
                F_Toy factory = new F_Toy();
                factory.updateStock(toyId, quantitySold);
            }

            if (!erroresEnDetalles) {
                JOptionPane.showMessageDialog(this, "¡Venta registrada con éxito!");
                resetSaleForm(); // Método para limpiar todo (ver abajo)
            } else {
                JOptionPane.showMessageDialog(this, "Venta guardada, pero hubo errores en algunos detalles.", "Advertencia", JOptionPane.WARNING_MESSAGE);
            }

        } else {
            JOptionPane.showMessageDialog(this, "Error de conexión al registrar la factura.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void tblCartMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblCartMouseClicked
        // TODO add your handling code here:
        int fila = tblCart.getSelectedRow();
        if (fila >= 0) {
            // 1. Recuperamos el ID y lo guardamos en la variable global
            selectedToyId = Integer.parseInt(tblCart.getValueAt(fila, 0).toString());

            // 2. Llenamos los campos de texto con los datos de la fila
            txtToy.setText(tblCart.getValueAt(fila, 1).toString());
            txtPrice.setText(tblCart.getValueAt(fila, 2).toString());
            txtQuantity.setText(tblCart.getValueAt(fila, 3).toString());
            txtTax.setText("15");

            // Consultamos la base de datos para obtener el stock más reciente
            F_Toy factory = new F_Toy();
            E_Toy toyData = factory.getToy(selectedToyId);

            if (toyData != null) {
                txtStock.setText(String.valueOf(toyData.getStock()));
            }
        }
    }//GEN-LAST:event_tblCartMouseClicked


