    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (txtDescription.getText().trim().isEmpty() 
                || txtAge.getText().trim().isEmpty()
                || txtCategory.getText().trim().isEmpty()
                || txtBrand.getText().trim().isEmpty()
                || txtSupplier.getText().trim().isEmpty()
                || txtSalePrice.getText().trim().isEmpty()
                || txtPurchasePrice.getText().trim().isEmpty()) {

            JOptionPane.showMessageDialog(this, "Todos los campos son obligatorios. Por favor, complete la información.");
            return;
        }

        try {
            toy.setId(Integer.parseInt(txtId.getText())); 
            toy.setDescription(txtDescription.getText());
            toy.setRecommendedAge(Integer.parseInt(txtAge.getText())); 
            toy.setCategory(txtCategory.getText());
            toy.setBrand(txtBrand.getText());
            toy.setSupplier(txtSupplier.getText());
            toy.setSalePrice(Double.parseDouble(txtSalePrice.getText()));
            toy.setPurchasePrice(Double.parseDouble(txtPurchasePrice.getText()));

            if (isEditing) {
                if (factory.update(toy)) {
                    JOptionPane.showMessageDialog(this, "Juguete actualizado con éxito.");
                    resetForm();
                }
            } else {
                // Aunque no haya campo en el form, enviamos 0 a la entidad para el insert
                toy.setStock(0); 
                if (factory.insert(toy)) {
                    JOptionPane.showMessageDialog(this, "Juguete registrado con éxito.");
                    resetForm();
                }
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor, introduce valores numéricos válidos en los precios.");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

