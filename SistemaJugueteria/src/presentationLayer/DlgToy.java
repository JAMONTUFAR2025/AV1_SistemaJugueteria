    private void tblToysMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblToysMouseClicked
        // TODO add your handling code here:
        int filaSelec = tblToys.getSelectedRow();
        if (filaSelec >= 0) {
            // Llenamos solo los campos que existen en tu formulario
            txtId.setText(tblToys.getValueAt(filaSelec, 0).toString());
            txtDescription.setText(tblToys.getValueAt(filaSelec, 1).toString());
            
            String ageFromTable = tblToys.getValueAt(filaSelec, 2).toString();
            txtAge.setText(ageFromTable.replace("+", "").trim());
            
            txtCategory.setText(tblToys.getValueAt(filaSelec, 3).toString());
            txtBrand.setText(tblToys.getValueAt(filaSelec, 4).toString());
            txtSupplier.setText(tblToys.getValueAt(filaSelec, 5).toString());
            txtSalePrice.setText(tblToys.getValueAt(filaSelec, 6).toString());
            txtPurchasePrice.setText(tblToys.getValueAt(filaSelec, 7).toString());

            // El stock (columna 8) se ignora porque no tienes donde mostrarlo
            isEditing = true; 
            
            // Si hacen doble clic, mandamos el objeto de vuelta
            if (evt.getClickCount() == 2 && listener != null) {
                E_Toy selected = new E_Toy();
                selected.setId(Integer.parseInt(tblToys.getValueAt(filaSelec, 0).toString()));
                selected.setDescription(tblToys.getValueAt(filaSelec, 1).toString());
                selected.setSalePrice(Double.parseDouble(tblToys.getValueAt(filaSelec, 6).toString()));
                selected.setStock(Integer.parseInt(tblToys.getValueAt(filaSelec, 8).toString()));
                // No son necesarios mas campos
                
                listener.onObjectSelected(selected);
                this.dispose();
            }
        }
    }//GEN-LAST:event_tblToysMouseClicked

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (!txtId.getText().isEmpty() && isEditing) {
            int confirm = JOptionPane.showConfirmDialog(this, "¿Desea eliminar este producto?", "Confirmar", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                int id = Integer.parseInt(txtId.getText());
                if (factory.delete(id)) {
                    JOptionPane.showMessageDialog(this, "Producto eliminado.");
                    resetForm();
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un registro de la tabla para eliminar.");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

