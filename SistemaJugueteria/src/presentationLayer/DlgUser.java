    private void tblUsersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsersMouseClicked
        // TODO add your handling code here:
        int filaSelec = tblUsers.getSelectedRow();
        if (filaSelec >= 0) {
            // Asignar a los campos de texto
            txtId.setText(tblUsers.getValueAt(filaSelec, 0).toString());
            txtFirstName.setText(tblUsers.getValueAt(filaSelec, 1).toString());
            txtLastName.setText(tblUsers.getValueAt(filaSelec, 2).toString());
            txtUsername.setText(tblUsers.getValueAt(filaSelec, 3).toString());
            txtPassword.setText(tblUsers.getValueAt(filaSelec, 4).toString());
            cboRole.setSelectedItem(tblUsers.getValueAt(filaSelec, 5).toString());

            // Importante: No dejar que el usuario piense que es un nuevo registro
            txtId.setEditable(false); 
            isEditing = true; // <--- Cambiamos a modo edición
        }
    }//GEN-LAST:event_tblUsersMouseClicked

