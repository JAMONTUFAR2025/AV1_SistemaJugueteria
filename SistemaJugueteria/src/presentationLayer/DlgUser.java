    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(txtFirstName.getText().trim().isEmpty() 
                || txtLastName.getText().trim().isEmpty()
                || txtUsername.getText().trim().isEmpty()
                || txtPassword.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Llene los campos obligatorios.");
            return;
        }

        user.setFirstName(txtFirstName.getText());
        user.setLastName(txtLastName.getText());
        user.setUsername(txtUsername.getText());
        user.setPassword(txtPassword.getText());
        
        String selectedRole = cboRole.getSelectedItem().toString();
        user.setRole(selectedRole);

        if (isEditing) {
            int selectedId = Integer.parseInt(txtId.getText());
            user.setId(selectedId);
            
            // --- VALIDACIÓN DE SEGURIDAD ---
            if (selectedId == loggedUser.getId() && !selectedRole.equals(loggedUser.getRole())) {
                JOptionPane.showMessageDialog(this, "Acción denegada: No puedes cambiar tu propio nivel de acceso.", "Seguridad", JOptionPane.ERROR_MESSAGE);
                // Opcional: Regresamos visualmente el ComboBox al rol correcto
                cboRole.setSelectedItem(loggedUser.getRole());
                return; // Detenemos el guardado
            }
            // -------------------------------
            
            if (factory.update(user)) {
                JOptionPane.showMessageDialog(this, "Usuario actualizado con éxito.");
                resetForm();
            }
        } else {
            // INSERTAR: Es un registro nuevo
            if (factory.insert(user)) {
                JOptionPane.showMessageDialog(this, "Usuario registrado con éxito.");
                resetForm();
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (!txtId.getText().isEmpty() && isEditing) {
            int selectedId = Integer.parseInt(txtId.getText());
        
            // --- VALIDACIÓN DE SEGURIDAD ---
            if (selectedId == loggedUser.getId()) {
                JOptionPane.showMessageDialog(this, "Acción denegada: No puedes eliminar tu propia cuenta.", "Seguridad", JOptionPane.ERROR_MESSAGE);
                return; // Detiene la ejecución del método aquí mismo
            }
            // -------------------------------
            
            int confirm = JOptionPane.showConfirmDialog(this, "¿Seguro que desea eliminar este usuario?", "Confirmar", javax.swing.JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                int id = Integer.parseInt(txtId.getText());
                if (factory.delete(id)) {
                    JOptionPane.showMessageDialog(this, "Usuario eliminado.");
                    cleanFields();
                    loadTable("");
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un usuario de la tabla para eliminar.");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

